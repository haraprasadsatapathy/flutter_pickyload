// Load local.properties to read Google Maps API Key
val localProperties = Properties()
val localPropertiesFile = rootProject.file("local.properties")
if (localPropertiesFile.exists()) {
    localPropertiesFile.inputStream().use { localProperties.load(it) }
}

val googleMapsApiKey = localProperties.getProperty("GOOGLE_MAPS_API_KEY") ?: ""

// Debug: Print API key status (only first/last 4 chars for security)
if (googleMapsApiKey.isNotEmpty()) {
    val maskedKey = googleMapsApiKey.take(4) + "..." + googleMapsApiKey.takeLast(4)
    println("✓ Google Maps API Key loaded: $maskedKey (length: ${googleMapsApiKey.length})")
} else {
    println("✗ ERROR: Google Maps API Key is EMPTY! Check local.properties")
}

